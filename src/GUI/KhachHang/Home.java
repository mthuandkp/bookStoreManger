    private void performEvent() {
        ActionListener ac = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                //Chuyển tất cả dữ liệu từ Sản phẩm -> Giỏ hàng
                ArrayList<ChiTietHoaDon> data = cthdbus.appendDetailBill(product.getCartData(), cart.getDataCart());
                cart = new CartShopping(data,getKh());
                product.setCartData(new ArrayList<>());
                setting.setVisible(false);
                switch (e.getActionCommand()) {
                    case "Trang Chủ": {
                        hideAllButton();
                        addJPanelIntoContent(trangchu);
                        break;
                    }
                    case "Sản Phẩm": {
                        showAllButton();
                        addJPanelIntoContent(product);
                        break;
                    }
                    case "Giỏ Hàng": {
                        showAllButton();
                        addJPanelIntoContent(cart);
                        break;
                    }
                    case "Lịch Sử": {
                        showAllButton();
                        lichsu = new LichSu(kh);
                        addJPanelIntoContent(lichsu);
                        break;
                    }
                }
            }
        };
        Arrays.stream(background.getComponents()).forEach(
                c -> {
                    if (c instanceof JButton) {
                        ((JButton) c).addActionListener(ac);
                        ((JButton) c).setActionCommand(((JButton) c).getActionCommand());
                    }
                }
        );
        
        Arrays.stream(content.getComponents()).forEach(c->{
            if(c instanceof TrangChu){
                Arrays.stream(((JPanel)c).getComponents()).forEach(c1->{
                    Arrays.stream(((JPanel)c1).getComponents()).forEach(c2->{
                        if(c2 instanceof JButton){
                            ((JButton)c2).addActionListener(ac);
                            ((JButton)c2).setActionCommand(((JButton)c2).getActionCommand());
                        }
                    });
                });
            }
        });

        this.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                ArrayList<ChiTietHoaDon> data = cthdbus.appendDetailBill(product.getCartData(), cart.getDataCart());
                cart = new CartShopping(data,kh);
                product.setCartData(new ArrayList<>());
                cart.recoveryNumberBook();
                System.exit(0);
            }
        });
        suakh.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                kh = khbus.getKHById(kh.getMaKhachHang());
                new Home(kh, null).setVisible(true);
                dispose();
            }
        });
        
    }

    private void addJPanelIntoContent(JPanel p) {
        content.removeAll();
        content.revalidate();
        content.repaint();
        p.setSize(1200, 640);
        p.setVisible(true);
        content.add(p);
    }

