    private void performSetting() {
        settingLabel.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent e){
                if(setting.isVisible()){
                    setting.setVisible(false);
                }
                else{
                    setting.setVisible(true);
                }
            }        
        });
        
        ActionListener ac = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                setting.setVisible(false);
                switch(e.getActionCommand()){
                    case "Đăng nhập":{
                        if(kh != null){
                            JOptionPane.showMessageDialog(null, "Bạn đã đăng nhập");
                            return;
                        }
                        new Login(cart.getDataCart()).setVisible(true);
                        dispose();
                        break;
                    }
                    case "Đăng ký":{
                        if(kh != null){
                            JOptionPane.showConfirmDialog(null, "Bạn cần đăng xuất trước khi đăng ký");
                            return;
                        }
                        new Register_1(cart.getDataCart()).setVisible(true);
                        dispose();
                        break;
                    }
                    case "Đổi mật khẩu":{
                        if(kh == null){
                            JOptionPane.showMessageDialog(null, "Bạn chưa đăng nhập");
                            return;
                        }
                        new changePassword(kh,cart.getDataCart()).setVisible(true);
                        dispose();
                        break;
                    }
                    case "Đăng xuất":{
                        if(kh == null){
                            JOptionPane.showMessageDialog(null, "Bạn chưa đăng nhập");
                            return;
                        }
                        new Home(null,cart.getDataCart()).setVisible(true);
                        dispose();
                        
                        break;
                    }
                    case "Sửa thông tin":{
                        if(kh == null){
                            JOptionPane.showMessageDialog(null, "Bạn chưa đăng nhập");
                            return;
                        }
                        suakh = new SuaKhachHang(kh);
                        suakh.setVisible(true);
                        break;
                    }
                }
            }
        };
        for(Component c : setting.getComponents()){
            if(c instanceof JButton){
                ((JButton)c).addActionListener(ac);
                ((JButton)c).setActionCommand(((JButton)c).getActionCommand());
            }
        }
    }
    
    public void hideAllButton(){
        for(Component c : background.getComponents()){
            if(c instanceof JButton){
                ((JButton)c).setVisible(false);
            }
        }
    }
    public void showAllButton(){
        for(Component c : background.getComponents()){
            if(c instanceof JButton){
                ((JButton)c).setVisible(true);
            }
        }
    }
