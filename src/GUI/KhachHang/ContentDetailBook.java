    private void loadBookInfo() {
        if(this.s == null) return;
        bookId.setText("Mã Sách:" + s.getMaSach());
        bookName.setText("Tên Sách : " + s.getTenSach());
        bookNXB.setText("Nhà Xuất Bản : " + s.getNhaXuatBan());
        bookType.setText("Thể Loại : " + tlbus.getNameTypeById(s.getMaTheLoai()));
        bookAuthor.setText("Tác Giả : " + s.getTacGia());
        bookNumber.setText("Số Lượng : " + sachbus.getNumberBookById(s.getMaSach()));
        String priceOfBook = Other.convetNumberToMoney(String.valueOf(s.getGia()));
        if(s.getPhanTramGiamGia() == 0){
            bookPrice.setText("Giá Bán : " + priceOfBook + " VNĐ");
        }
        else{
            String priceOfBookSale = Other.convetNumberToMoney(String.valueOf((int)(s.getGia()*(1-s.getPhanTramGiamGia()/100.0))));
            bookPrice.setForeground(Color.red);
            bookPrice.setText("Giá Bán : " + priceOfBookSale + "(" + priceOfBook + ") VNĐ");
        }
        
        bookDecrease.setText("-" + s.getPhanTramGiamGia() + "%");
        bookImage.setSize(138, 169);
        bookImage.setIcon(new ImageIcon(CopyImage.resizeImage(".\\src\\Book_Image\\" + s.getHinhAnh(), bookImage)));
        
        bookName.setEditable(false);
        bookNXB.setEditable(false);
        bookType.setEditable(false);
        bookAuthor.setEditable(false);
        bookNumber.setEditable(false);
        bookPrice.setEditable(false);
        
        
        bookName.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
        bookNXB.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
        bookType.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
        bookAuthor.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
        bookNumber.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
        bookPrice.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, new Color(65,247,247)));
    }
