    private void drawChart(int year) {
        ArrayList<String []> data = hdbus.getDataBarChart(year);
        ArrayList<String []> dataMonth = hdbus.getDataLineChart(year);
        ProcessingFunction.ThongKe.barChart(data, "Thống kê hóa đơn theo tổng tiền", "Hóa đơn", "Tổng", barChart);
        ProcessingFunction.ThongKe.barChart(dataMonth, "Thống kê Hóa đơn theo tháng", "Tháng", "Tổng(Triệu VNĐ)", barChartMonth);
    }

    private void performEvent() {
        statisBtn.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                

                if(inputSearch.getText().compareTo("") == 0){
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập năm cần tìm");
                    return;
                }
                try {
                    if(Integer.valueOf(inputSearch.getText()) < 1){
                        throw new Exception();
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Năm phải là số nguyên > 0");
                    return;
                }
                int year = Integer.valueOf(inputSearch.getText());
                yearLabel.setText("Năm : " + year);
                ProcessingFunction.Other.removeAllInPanel(barChart);
                ProcessingFunction.Other.removeAllInPanel(barChartMonth);
                drawChart(year);
            }
        });
    }
    
    
